package FunctionLayer;

/**
 *
 * @author simon
 */
public class Screw {
    int size;

    public Screw(int size) {
        this.size = size;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    @Override
    public String toString() {
        return "Screw{" + "size=" + size + '}';
    }

        
}
