package FunctionLayer;

/**
 *
 * @author simon
 */
public class Roofplate400x400 {
    int width = 400;
    int length = 400;

    public Roofplate400x400(int width, int length) {
        this.width = width;
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
    
    
}
