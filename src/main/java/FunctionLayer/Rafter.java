package FunctionLayer;

/**
 *
 * @author simon
 */
public class Rafter {
    int length, width;

    public Rafter(int length, int width) {
        this.length = length;
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    @Override
    public String toString() {
        return "Rafter{" + "length=" + length + ", width=" + width + '}';
    }
    
    
}
